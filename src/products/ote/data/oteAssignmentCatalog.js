function toNativeRoute(routePath = "") {
  return routePath.startsWith("/ote/") ? routePath.replace(/^\/ote/, "") || "/" : routePath;
}

export function getOteAssignmentItems({ variant = "general", nativeRoutes = false } = {}) {
  const normalizedVariant = variant === "advanced" ? "advanced" : "general";
  return OTE_ASSIGNMENT_ITEMS.filter((item) => item.variant === normalizedVariant).map((item) => {
    const routePath = nativeRoutes ? toNativeRoute(item.routePath) : item.routePath;
    return {
      ...item,
      routePath: getSitePath(routePath),
    };
  });
}

export function getOteAssignmentItem(itemId, options = {}) {
  return getOteAssignmentItems(options).find((item) => item.id === itemId) || null;
}
